class Ex01{
 public static void main(String[] args){
	// 강제형변환
	long su0;
	su0 = 1234; // 자동형변환 int -> long
	float su1;
	su1 = (float)3.14; // 강제형변환 double -> float
	System.out.println(su1);
	int su2;
	su2 = (int)3.14;
	System.out.println(su2);
	byte su3;
	su3 = (byte)129;
	System.out.println(su3);
	int su4 = 1234;
	System.out.println(su4);
 }
}