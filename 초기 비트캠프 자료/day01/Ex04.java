class Ex04{
public static void main(String[] args){
	// 숫자(정수형)
	System.out.println(12340000);
	// 숫자(실수형)
	System.out.println(3.140000000000000001);
	// 문자
	// 2바이트 유니코드사용	
	System.out.println('A');
	System.out.println(' '); //띄어쓰기는 해야 출력됨.(공백문자)
	// 문자열
	// 길이제한없음
	System.out.println("문자열-길이제한없음");
	// 문자열은 집합이기 때문에 문자열 아예 안써도 됨
	System.out.println("");
}
}
