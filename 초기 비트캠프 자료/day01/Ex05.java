class Ex05{
public static void main(String[] args){
	// 10진수 정수
	System.out.println(1+2+3+4+5);
	System.out.println(1+2*3+4+5);
	System.out.println(4/2); 	//=2
	System.out.println(4%2);	//=0
	System.out.println(5/2); 	//=2
	System.out.println(5%2); 	//=1
	// 10진수 실수
	System.out.println(4.0/2.0);	//=2.0
	System.out.println(7.0/3.0);	//=2.3333333333333335
	System.out.println(4/2.0);	//=2.0
	System.out.println(7/3.0);	//=2.3333333333333335
	System.out.println(4.0%2.0);	//=0.0
	System.out.println(5.0%2.0);	//=1.0
}
}